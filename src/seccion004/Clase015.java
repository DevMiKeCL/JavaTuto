package seccion004;

public class Clase015 {
    public static void main(String[] args) {
        byte b1 = 10;
        System.out.println("b1 = " + b1);

        //permite el ingreso del sistema hexadecimal
        byte b2 = 0xa;
        System.out.println("b2 = " + b2);

        //permite el ingreso del sistema octal
        byte b3 = 012;
        System.out.println("b3 = " + b3);
    }
}
