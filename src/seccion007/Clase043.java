package seccion007;

public class Clase043 {
    public static void main(String[] args) {
        var i = 0;
        while (i <= 3){
            System.out.println("i = " + i);
            i++;
        }

        i = 0;
        do {
            System.out.println("i = " + i);
            i++;
        }while (i < 3);
    }
}
