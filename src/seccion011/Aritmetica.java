package seccion011;

public class Aritmetica {
    //Atributos de la clase
    int a;
    int b;

    //Constructor sin argumentos
    Aritmetica(){

    }

    //Contructor con argumentos
    Aritmetica(int arg1, int arg2){
        a = arg1;
        b = arg2;
    }


    int sumar(){
        return a + b;

    }
}
